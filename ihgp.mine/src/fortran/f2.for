C
      PRINT *, 'ENTER COEFFICIENT A: '
      READ *, A
      PRINT *, 'ENTER COEFFICIENT B: '
      READ *, B
      PRINT *, 'ENTER COEFFICIENT C: '
      READ *, C
C
C CALCULATE X1 AND X2.
C
      PRINT *, 'ROOTS ARE: ', X1, X2
      X1 = (-B + (B**2 - 4*A*C)**0.5)/(2*A)
      X2 = (-B - (B**2 - 4*A*C)**0.5)/(2*A)
      PRINT *, 'ROOTS ARE: ', X1, X2
C
C ALL DONE
C
      END
